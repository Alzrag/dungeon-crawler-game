// ---------- MAP GENERATION ----------
vector<vector<char>> generate_map(int width, int height, unsigned int seed) {
    int rows = height * 2 + 1;
    int cols = width * 2 + 1;
    vector<vector<char>> room(rows, vector<char>(cols, '#'));
    mt19937 gen(seed);

    int start_row = 1, start_col = 1;
    room[start_row][start_col] = ' ';
    stack<pair<int,int>> cellStack;
    cellStack.push({start_row, start_col});

    vector<pair<int,int>> dirs = {{-2,0},{2,0},{0,-2},{0,2}};
    while (!cellStack.empty()) {
        auto [r,c] = cellStack.top();
        vector<pair<int,int>> neighbors;
        for (auto [dr,dc]: dirs) {
            int nr = r + dr, nc = c + dc;
            if (nr > 0 && nr < rows-1 && nc > 0 && nc < cols-1 && room[nr][nc]=='#') neighbors.push_back({nr,nc});
        }
        if (!neighbors.empty()) {
            uniform_int_distribution<int> dist(0, neighbors.size()-1);
            auto [nr,nc] = neighbors[dist(gen)];
            room[(r+nr)/2][(c+nc)/2] = ' ';
            room[nr][nc] = ' ';
            cellStack.push({nr,nc});
        } else cellStack.pop();
    }

    room[1][1] = '!';

    // goal
    while(true) {
        uniform_int_distribution<int> row_dist(0,(int)room.size()-1);
        int r=row_dist(gen);
        uniform_int_distribution<int> col_dist(0,(int)room[0].size()-1);
        int c=col_dist(gen);
        if(room[r][c]==' '){ room[r][c]='X'; break; }
    }

    // monsters
    for(int i=0;i<3;i++){
        while(true){
            uniform_int_distribution<int> row_dist(0,(int)room.size()-1);
            int r=row_dist(gen);
            uniform_int_distribution<int> col_dist(0,(int)room[0].size()-1);
            int c=col_dist(gen);
            if(room[r][c]==' '){ room[r][c]='?'; break; }
        }
    }
    return room;
}

// ---------- PRINT TOP-DOWN MAP ----------
void print_map(const vector<vector<char>>& room){
    cout << "=== Top-Down Map ===\n";
    for(const auto& row:room){
        for(char c:row) cout << c;
        cout << '\n';
    }
}

void fill_if_wall_ahead(const vector<vector<char>>& room, int pr, int pc, int facing, vector<string>& buffer) {
   

    // Determine delta based on facing
    int dr = 0, dc = 0;
    switch(facing) {
        case 5: dr = -1; break; // up
        case 2: dr = 1;  break; // down
        case 1: dc = -1; break; // left
        case 3: dc = 1;  break; // right
    }

    // --- Check 2 tiles ahead ---
    int r2 = pr + 2*dr;
    int c2 = pc + 2*dc;

    if(r2 >= 0 && r2 < room.size() && c2 >= 0 && c2 < room[0].size() && room[r2][c2] == '#') {
        // Fill the smaller rectangle for 2-tile-ahead wall
        for(int r = 14; r <= 24 && r < buffer.size(); r++) {
            for(int c = 45; c <= 82 && c < buffer[r].size(); c++) {
                buffer[r][c] = '#';
            }
        }
    }

    // --- Check 1 tile ahead ---
    int r1 = pr + dr;
    int c1 = pc + dc;

    if(r1 >= 0 && r1 < room.size() && c1 >= 0 && c1 < room[0].size() && room[r1][c1] == '#') {
        // Fill the larger rectangle for 1-tile-ahead wall
        for(int r = 9; r <= 27 && r < buffer.size(); r++) {
            for(int c = 28; c <= 98 && c < buffer[r].size(); c++) {
                buffer[r][c] = '#';
            }
        }
    }

    // Print buffer for visualization every move
    cout << "=== 3D Wall Ahead Visualization ===\n";
    for(const auto& line : buffer) {
        cout << line << "\n";
    }
}



